package com.bl.ds;

public class SquareOfIntegerNormalWay {
	
	public int squareIt(int x) {
		return x*x;
	}

public static void main(String[] args) {
	SquareOfIntegerNormalWay squareOfIntegerNormalWay =new SquareOfIntegerNormalWay();
	System.out.println("The square of 20 is : "+squareOfIntegerNormalWay.squareIt(20));
	System.out.println("The square of 50 is : "+squareOfIntegerNormalWay.squareIt(50));
}
}
